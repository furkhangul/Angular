/* 
SPA (Single Page Application) Nedir ?
Adı üzerinde tek sayfa uygulamaları.
SPA'nın mantığı biz bir siteye girdiğimiz zaman bir defa site yükleniyor ve bir daha site yüklenme gereği duymuyor.
Kullanıcı sayfalar arasında gezinmek istediğinde backend'den tüm sayfanın gelmesi yerine örneğin header den bir değişiklik ile sadece header kısmı güncellenir.

Klasik yaklaşımda ise tüm sayfa her bir değişiklik için tekrardan yüklenmekteydi.
*/


